   public void cargarFormulario() {
        
        int fila = tblusuario.getSelectedRow();
        
        txtidusuario.setText(tblusuario.getValueAt(fila, 0).toString());
        txtnombre.setText(tblusuario.getValueAt(fila, 1).toString());
        txtapellido.setText(tblusuario.getValueAt(fila, 2).toString());
        txtcedula.setText(tblusuario.getValueAt(fila, 3).toString());
        txttelefono.setText(tblusuario.getValueAt(fila, 4).toString());
        txtdireccion.setText(tblusuario.getValueAt(fila, 5).toString());
        txtemail.setText(tblusuario.getValueAt(fila, 6).toString());
        
    }
    
