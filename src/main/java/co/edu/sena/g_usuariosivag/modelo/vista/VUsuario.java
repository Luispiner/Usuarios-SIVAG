    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        int idusuario= Integer.parseInt(txtidusuario.getText());
        int cedula = Integer.parseInt(txtcedula.getText());
        int telefono = Integer.parseInt(txttelefono.getText ());
     
        String nombre = txtnombre.getText();
        String apellido = txtapellido.getText();
        String direccion = txtdireccion.getText();
        String email = txtemail.getText();
        
        
        Usuario e = new Usuario(Integer.parseInt(txtidusuario.getText()), txtnombre.getText(), txtapellido.getText(), Integer.parseInt(txtcedula.getText()), Integer.parseInt(txttelefono.getText()), txtdireccion.getText(), txtemail.getText());

        CUsuario control = new CUsuario ();
        control.insertarUsuario (e);
        
        limpiarPantalla (); 
        
        
        
    }//GEN-LAST:event_btnInsertarActionPerformed

